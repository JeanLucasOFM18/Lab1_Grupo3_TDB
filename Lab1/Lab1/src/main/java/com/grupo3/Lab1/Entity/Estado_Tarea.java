package com.grupo3.Lab1.Entity;

public class Estado_Tarea {

    private Integer id;
    private String estado;

    public Estado_Tarea(Integer id, String estado) {
        this.id = id;
        this.estado = estado;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
}
